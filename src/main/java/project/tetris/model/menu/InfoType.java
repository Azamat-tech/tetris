package project.tetris.model.menu;

/**
 * Information type that is used to navigate in menu
 *
 * {@link #HELP}
 * {@link #SCOREBOARD}
 * {@link #NONE}
 */
public enum InfoType {

    /**
     * Help page
     */
    HELP,
    /**
     * Leaderboard page
     */
    SCOREBOARD,
    /**
     * Neither help nor leaderboard
     */
    NONE
}
